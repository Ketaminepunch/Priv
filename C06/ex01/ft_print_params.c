#include <unistd.h>


int	main(int count, char **vector)
{
	int i;
	int j;

	
	j = 1;	
	while(j < count)
	{
		i = 0;
		while (vector[j][i])
		{
			write(1, &vector[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}