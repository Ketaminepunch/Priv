#include <unistd.h>

int	main(int count, char **vector)
{
	int i;

	i = 0;
	if(count == 1)
	{
		while(vector[0][i])
		{
			write(1, &vector[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}