#include <unistd.h>


int	main(int count, char **vector)
{
	int i;
	int j;

	
	j = count - 1;
	while(j > 0)
	{
		i = 0;
		while (vector[j][i])
		{
			write(1, &vector[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		j--;
	}
}